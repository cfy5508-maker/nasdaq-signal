// ── 점선 계산 ─────────────────────────────────────
var SCALE=nd_monthly[0]/hy_monthly[0]; // 7001/3.32
function getYear(l){return 2000+parseInt(l.substring(0,2));}
function getMG(l){var c=CAGR_MAP[getYear(l)]||0.160;return Math.pow(1+c,1/12);}
var hyLine=[],cum=1.0;
for(var i=0;i<N;i++){if(i>0)cum*=getMG(labels[i]);hyLine.push(hyOas[i]*SCALE*cum);}

// ── 이격도 계산 ───────────────────────────────────
// 양수 = 점선 위 = 저평가(매수), 음수 = 점선 아래 = 고평가(매도)
var gap=hyLine.map(function(v,i){
  return parseFloat(((v-nasdaq[i])/nasdaq[i]*100).toFixed(1));
});

// ── 신호 타점 계산 (우선순위: 매도>경고>관망>매수>강한매수) ──
(function(){
  var sbuy=[],buy=[],watch=[],warn=[],sell=[];
  var ps=false,pb=false,pw=false,pn=false;

  for(var i=0;i<N;i++){
    var g=gap[i],h=hyOas[i];
    var gp=i>0?gap[i-1]:g;

    // 원시 조건
    var isSell =h<3.0&&g<-20;
    var isWarn =h<3.0&&-20<=g&&g<0;
    var isCross=h>=3.0&&((gp<=0&&g>0)||(gp>0&&g<=0));
    var isBuy  =g>20&&h>=3.5&&h<4.5;
    var isSbuy =g>20&&h>=4.5;

    // 우선순위 적용
    var sig=null;
    if(isSell)       sig='sell';
    else if(isWarn)  sig='warn';
    else if(isCross) sig='watch';
    else if(isBuy)   sig='buy';
    else if(isSbuy)  sig='sbuy';

    // 연속 중복 제거 (첫 진입만)
    if(sig==='sell' &&!ps){sell.push(i);}
    if(sig==='warn' &&!pn){warn.push(i);}
    if(sig==='watch')      {watch.push(i);}  // 관망은 전환 시점마다
    if(sig==='buy'  &&!pb){buy.push(i);}
    if(sig==='sbuy' &&!ps){sbuy.push(i);}

    ps=(sig==='sell'||sig==='sbuy');
    pb=(sig==='buy');
    pn=(sig==='warn');
  }

  window.SIGNALS={sbuy:sbuy,buy:buy,watch:watch,warn:warn,sell:sell};
})();

